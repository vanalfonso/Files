#include <iostream>
//Using NESTED IF//
using namespace std; 
int main()
{
int number;
cout <<"Choose a number from 1 - 5 : " ;
cin >> number;
	if (number == 1)
	{
	cout <<"You picked number 1" ;
	}
	else if (number == 2)
	{
	cout<<"You picked number 2! " ;
	}
	else if (number == 3)
	{
	cout<<"You picked number 3! " ;
	}
	else if (number == 4)
	{
	cout<<"You picked number 4 " ;
	}
	else
	{
	cout<<"You picked number 5 " ;
	}
}
